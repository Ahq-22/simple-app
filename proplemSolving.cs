using System;

public class program
{
    public static int multipying(int num1)
    {
        if (num1 % 2 == 0)
        {
            return num1 * 8;
        }
        else
        {
            return num1 * 9
        }
    }
    public static void Main(string[] args)
    {
        multipying(4);
    }
}
